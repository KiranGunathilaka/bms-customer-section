    private void CreateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CreateButtonActionPerformed
        // TODO add your handling code here:
        occupation = occupationField.getText();
        email = emailField.getText();
        deposit = Double.valueOf(depositField.getText());
        if (emptyFieldsChecker() || depositChecker(deposit)) {
            String query3 = "INSERT INTO `savingsaccounts` ( `UserIndex`, `Email`, `Occupation`, `Balance`) VALUES ( ?, ?, ?, ?)";
            PreparedStatement pst;

            try {
                pst = conn.prepareStatement(query3);
                pst.setInt(1, id);
                pst.setString(2, email);
                pst.setString(3, occupation);
                pst.setDouble(4, deposit);
                if (pst.executeUpdate() != 0) {
                    JOptionPane.showMessageDialog(null, "Your account has been Created.");
                    MainMenu form = new MainMenu();
                    form.setVisible(true);
                    form.pack();
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "Something went wrong.\nPlease check your information and try again", "Eror", 3);
                }

            } catch (SQLException ex) {
                Logger.getLogger(CreateSavingsAccount.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_CreateButtonActionPerformed

