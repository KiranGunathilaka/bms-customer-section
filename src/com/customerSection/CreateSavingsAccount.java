    public boolean emptyFieldsChecker() {
        if (occupation.equals("") || email.equals("") || depositField.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "One or more fields are empty", "Warning", 0);
            return false;
        } else {
            return true;
        }
    }

    public boolean depositChecker(double deposit) {
        if (this.deposit <= 1000) {
            JOptionPane.showMessageDialog(null, "Initial deposit amount must be greater than 1000 Rupees", "Message", 2);
            depositChecker = false;

        } else {
            depositChecker = false;
        }
        return depositChecker;
    }

